@router.put("/users/{user_id}", response_model=UserResponse)
async def update_user(
    user_id: UUID,
    request: UserUpdateRequest,
    current_user: Annotated[User, Depends(get_current_superuser)],
    db: Annotated[AsyncSession, Depends(get_db)],
) -> UserResponse:
    """
    Update user properties.

    Requires superuser access.
    """
    try:
        result = await db.execute(select(User).where(User.id == user_id))
        user = result.scalar_one_or_none()

        if not user:
            raise HTTPException(
                status_code=status.HTTP_404_NOT_FOUND,
                detail="User not found",
            )

        # Update fields if provided
        if request.is_pro is not None:
            user.is_pro = request.is_pro
        if request.is_superuser is not None:
            user.is_superuser = request.is_superuser
        if request.plan_expiry_date is not None:
            user.plan_expiry_date = request.plan_expiry_date

        await db.commit()

        strategy_count_subq = (
            select(func.count(Strategy.id))
            .where(Strategy.user_id == User.id)
            .correlate(User)
            .scalar_subquery()
        )
        ai_report_count_subq = (
            select(func.count(AIReport.id))
            .where(AIReport.user_id == User.id)
            .correlate(User)
            .scalar_subquery()
        )

        # Reload user with subqueries to get counts
        result = await db.execute(
            select(
                User,
                strategy_count_subq.label("strategies_count"),
                ai_report_count_subq.label("ai_reports_count")
            )
            .where(User.id == user.id)
        )
        row = result.first()

        if not row:
            raise HTTPException(
                status_code=status.HTTP_404_NOT_FOUND,
                detail="User not found after update",
            )

        return UserResponse(
            id=row.User.id,
            email=row.User.email,
            is_pro=row.User.is_pro,
            is_superuser=row.User.is_superuser,
            subscription_id=row.User.subscription_id,
            plan_expiry_date=row.User.plan_expiry_date,
            daily_ai_usage=row.User.daily_ai_usage,
            created_at=row.User.created_at,
            strategies_count=row.strategies_count,
            ai_reports_count=row.ai_reports_count,
        )
    except HTTPException:
        raise
    except Exception as e:
        logger.error(f"Error updating user {user_id}: {e}", exc_info=True)
        await db.rollback()
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail="Failed to update user",
        )


@router.delete("/users/{user_id}", status_code=status.HTTP_204_NO_CONTENT)
async def delete_user(
    user_id: UUID,
    current_user: Annotated[User, Depends(get_current_superuser)],
    db: Annotated[AsyncSession, Depends(get_db)],
) -> None:
    """
    Delete a user and all associated data.

    Requires superuser access.
    
    Prevents deleting yourself.
    """
    try:
        if user_id == current_user.id:
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail="Cannot delete yourself",
            )
        
        result = await db.execute(select(User).where(User.id == user_id))
        user = result.scalar_one_or_none()
        
        if not user:
            raise HTTPException(
                status_code=status.HTTP_404_NOT_FOUND,
                detail="User not found",
            )
        
        # Delete associated data first to avoid foreign key constraint violations
        from app.db.models import Strategy, AIReport, GeneratedImage
        
        # Delete user's generated images
        images_result = await db.execute(
            select(GeneratedImage).where(GeneratedImage.user_id == user_id)
        )
        images = images_result.scalars().all()
        for image in images:
            await db.delete(image)

        # Delete user's strategies
        strategies_result = await db.execute(
            select(Strategy).where(Strategy.user_id == user_id)
        )
        strategies = strategies_result.scalars().all()
        for strategy in strategies:
            await db.delete(strategy)
        
        # Delete user's AI reports
        reports_result = await db.execute(
            select(AIReport).where(AIReport.user_id == user_id)
        )
        reports = reports_result.scalars().all()
        for report in reports:
            await db.delete(report)
        
        # Now delete the user (tasks will be deleted by cascade)
        await db.delete(user)
        await db.commit()
        
        logger.info(f"User {user_id} deleted by admin {current_user.id} (deleted {len(images)} images, {len(strategies)} strategies, {len(reports)} reports)")
    except HTTPException:
        raise
    except Exception as e:
        logger.error(f"Error deleting user {user_id}: {e}", exc_info=True)
        await db.rollback()
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail="Failed to delete user",
        )


