@router.get("/users", response_model=UsersListResponse)
async def list_users(
    current_user: Annotated[User, Depends(get_current_superuser)],
    db: Annotated[AsyncSession, Depends(get_db)],
    limit: int = Query(50, ge=1, le=1000, description="Maximum number of users per page"),
    skip: int = Query(0, ge=0, description="Number of users to skip"),
    search: str | None = Query(None, description="Search by email (case-insensitive partial match)"),
) -> UsersListResponse:
    """
    List all users (paginated with search).

    Requires superuser access.
    """
    try:
        # Subqueries for counts to avoid N+1 and cartesian products
        strategy_count_subq = (
            select(func.count(Strategy.id))
            .where(Strategy.user_id == User.id)
            .correlate(User)
            .scalar_subquery()
        )
        ai_report_count_subq = (
            select(func.count(AIReport.id))
            .where(AIReport.user_id == User.id)
            .correlate(User)
            .scalar_subquery()
        )

        # Build base query
        base_query = select(
            User,
            strategy_count_subq.label("strategies_count"),
            ai_report_count_subq.label("ai_reports_count")
        )
        
        # Apply search filter if provided
        if search and search.strip():
            search_term = f"%{search.strip()}%"
            base_query = base_query.where(User.email.ilike(search_term))
        
        # Count total (before pagination) - use scalar_subquery for proper counting
        count_query = select(func.count(User.id))
        if search and search.strip():
            search_term = f"%{search.strip()}%"
            count_query = count_query.where(User.email.ilike(search_term))
        total_result = await db.execute(count_query)
        total = total_result.scalar_one() or 0
        
        # Query users with subqueries for counting
        result = await db.execute(
            base_query
            .order_by(User.created_at.desc())
            .limit(limit)
            .offset(skip)
        )
        rows = result.all()

        return UsersListResponse(
            users=[
                UserResponse(
                    id=row.User.id,
                    email=row.User.email,
                    is_pro=row.User.is_pro,
                    is_superuser=row.User.is_superuser,
                    subscription_id=row.User.subscription_id,
                    plan_expiry_date=row.User.plan_expiry_date,
                    daily_ai_usage=row.User.daily_ai_usage,
                    created_at=row.User.created_at,
                    strategies_count=row.strategies_count,
                    ai_reports_count=row.ai_reports_count,
                )
                for row in rows
            ],
            total=total,
            page=skip // limit if limit > 0 else 0,
            limit=limit,
        )
    except Exception as e:
        logger.error(f"Error listing users: {e}", exc_info=True)
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail="Failed to list users",
        )


